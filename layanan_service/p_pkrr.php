                            <li class="mb-2">Memberdayakan Remaja: PKRR yang bersifat UKP bertujuan untuk memberdayakan remaja dalam pengambilan keputusan tentang kesehatan mereka sendiri, termasuk keputusan tentang hubungan seksual, kontrasepsi, dan perawatan kesehatan reproduksi lainnya.</li>
                        </ul>
