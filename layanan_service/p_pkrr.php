                            <li class="mb-2">Menyediakan Pengobatan dan Perawatan: PKRR yang bersifat UKP memberikan akses kepada remaja untuk mendapatkan pengobatan dan perawatan yang dibutuhkan untuk masalah kesehatan reproduksi mereka, termasuk pengobatan untuk IMS dan dukungan kesehatan mental.</li>
