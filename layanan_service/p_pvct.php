                            <li class="mb-2">Meningkatkan Kualitas Hidup: Dengan deteksi dini dan pengobatan yang tepat, pelayanan VCT/IMS membantu meningkatkan kualitas hidup individu yang terinfeksi dan mencegah komplikasi kesehatan jangka panjang.</li>
