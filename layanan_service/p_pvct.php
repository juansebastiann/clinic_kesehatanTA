                            <li class="mb-2">Pengobatan yang Tepat: Setelah diagnosis, pasien dapat menerima pengobatan yang sesuai untuk infeksi menular seksual yang mereka alami, yang penting untuk mencegah penyebaran lebih lanjut dan komplikasi.</li>
