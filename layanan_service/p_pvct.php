                            <li class="mb-2">Konseling Pra-Tes: Sebelum tes dilakukan, individu akan menerima konseling yang menjelaskan proses tes, risiko, dan manfaatnya, serta memberikan informasi tentang infeksi menular seksual dan pencegahannya.</li>
