                            <li class="mb-2">Dukungan Psikososial: Konseling yang disediakan melalui layanan VCT/IMS memberikan dukungan psikososial yang penting bagi individu yang terdiagnosis dengan infeksi menular seksual, membantu mereka mengelola kondisi mereka dengan lebih baik dan mendapatkan dukungan yang mereka butuhkan.</li>
                        </ul>
